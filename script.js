function getElements(category) {
    let cont = document.querySelector("#container");
    let out = "<div class='row'>";
    let i = 0;
    arr.forEach(elem => {
        if (category == null || elem.category == category) {
            out += "<div class='col-sm-4'><img class='img-fluid' src=" + elem.link + " alt=" + elem.name + "<b class='name'>"  + elem.description3 + "</b><p class='description'>" + elem.description + "</p><b class='price'>"+ elem.description2 + "</b></div>"
            i++;
        }
        if (i % 3 == 0 && 1) {
            out += "</div><div class='row'>";
        }
    });
    out += "</div>";
    cont.innerHTML = out;
}

